//a) Qual a diferença entre as estruturas pilha e fila?
//Pilha (LIFO): O último elemento inserido é o primeiro a ser removido.
//Fila (FIFO): O primeiro elemento inserido é o primeiro a ser removido.

//b) Quais as vantagens da fila? Quais suas desvantagens?
//Vantagens: Organiza a execução na ordem de chegada, útil em processos e filas de espera.
//Desvantagens: Acesso restrito (só ao início da fila), não permite acessar elementos no meio.

//c) Quais as vantagens da pilha? Quais suas desvantagens?
//Vantagens: Simples, eficiente para controle de execução (como recursão) e para expressões matemáticas.
//Desvantagens: Acesso limitado (só ao topo), risco de overflow.

//d) Quais as suas aplicações?
//Pilha: Recursão, funcionalidades de desfazer/refazer, avaliação de expressões e verificação de expressões balanceadas.
//Fila: Gerenciamento de processos, impressão e eventos em tempo real.

//e) Qual a diferença deles para a lista?
//Lista: Acesso livre a qualquer posição.
//Pilha: Acesso restrito ao topo (LIFO).
//Fila: Acesso restrito ao início (FIFO).







